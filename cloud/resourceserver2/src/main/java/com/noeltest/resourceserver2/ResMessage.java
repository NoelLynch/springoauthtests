package com.noeltest.resourceserver2;

public class ResMessage {
    private String msg;

    public ResMessage() {
        super();
    }
    
    public ResMessage(String msg) {
        super();
        this.msg = msg;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
